//! 9) for loop გამოიყენება განმეორებითი (iterative) ოპერაციებისათვის, როდესაც ვიცით რამდენჯერ უნდა შესრულდეს ციკლი.
//! 10) for-ის სინტაქსი: for (შესავალები; პირობა; ინკრემენტი) { /* code */ }

//! 11) for loop-ით 0 დან 10-მდე ნომრების გამოყვანა:
{
    for (let i = 0; i <= 10; i++) {
        console.log(i);
    }
}

//! 12) ცვლადი შენახეთ გარკვეული რიცხვი და for-ის გამოყენებით გამოიტანეთ 1-დან ამ ცვლამდე რიცხვები.
{
    const max = Number(prompt("გამოიტანე რიცხვები 1-დან რომელ რიცხვამდე?"));
    for (let i = 1; i <= max; i++) {
        console.log(i);
    }
}

//! 13) while loop არის ციკლი რომელიც მკვეთრად განმეორდება სანამ პირობა true იქნება — პირობა მიმდინარეობს ციკლის დასაწყისში.
//! 14) while loop-ით დაბეჭდეთ 1-დან 10-მდე:
{
    let i = 1;
    while (i <= 10) {
        console.log(i);
        i++;
    }
}

//! 15) დაბეჭდეთ მხოლოდ ლუწი რიცხვები 1-დან 20-მდე while loop-ით.
{
    let n = 1;
    while (n <= 20) {
        if (n % 2 === 0) {
            console.log(n);
        }
        n++;
    }
}

//! 16) do...while loop არის ციკლი რაც პირველ რიგში ასრულებს ბლოკს და შემდეგ ამოწმებს პირობას — მინიმუმ ერთხელ ყოველთვის შესრულდება.
//! 17) განსხვავება: while -> პირობა შემოწმდება თუნდაც 0-ჯერ შესრულებისთვის; do...while -> შესრულდება მინიმუმ 1-ჯერ და შემდეგ შემოწმდება პირობა.

//! 18) შექმენით πρόγραμμα, რომელიც ეთიანს კითხვას: "Do you like JavaScript?" და აგრძელებს მანამდე, სანამ პასუხი არ იქნება "yes" (without parse).
{
    let answer;
    do {
        answer = prompt('Do you like JavaScript? (type "yes" to stop)') || "";
        // case-insensitive შედარება
    } while (answer.toLowerCase() !== "yes");
    console.log('Glad you said yes!');
}
