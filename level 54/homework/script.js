//! 16) ახსენით do...while loop.
//! 17) რა განსხვავებაა while loopსა და do...while loopს შორის?
//! 18) შექმენი პროგრამა, რომელიც სვამს კითხვას:
//! “Do you like JavaScript?”
//! 1) switch ნიშნავს მრავალგზის არჩევანის კონტროლს — ვადარებთ ერთადერთ გამომხატველს სხვადასხვა case-ებთან და შევასრულებთ შესაბამის ბლოკს.
//! 2) შექმენით ცვლადი ასაკით და შეამოწიოთ switch-ით სრულწლოვნა (without parse).
{
    // ასაკი:
    const age = Number(prompt("შეიტანეთ თქვენი ასაკი:")); // Number() used instead of +prompt
    // switch-ის მაგალითი რთული ლოგიკისთვის: switch(true) -> შეძლებთ პირობების გამოყენებას
    switch (true) {
        case age >= 18:
            console.log("მომხმარებელი სრულწლოვანია.");
            break;
        case age >= 0:
            console.log("მომხმარებელი არასრულწლოვანია.");
            break;
        default:
            console.log("არასწორი ასაკი.");
    }
}

//! 3) ორი ცვლადი ფიგურის სიგრძე და სიგანე — switch-ით გავიგოთ კვადრატია თუ არა.
{
    const length = Number(prompt("შეიტანეთ ფიგურის სიგრძე:"));
    const width = Number(prompt("შეიტანეთ ფიგურის სიგანე:"));
    switch (length === width) {
        case true:
            console.log("ფიგურა არის კვადრატი.");
            break;
        case false:
            console.log("ფიგურა არ არის კვადრატი.");
            break;
        default:
            console.log("არასწორი მონაცემები.");
    }
}

//! 4) case switch-ში არის ინდივიდუალური შემოწმებადი ღირებულება — თუ switch-ის გამომხატველის მნიშვნელობა დაემთხვება case-ის სისწორეს, εκτελείται მისში არსებული კოდი.
//! 5) break აჩერებს switch-ის სხვა case-ების შემსრულებლობას — თავიდან ასაცილებლად არ გააგრძელდეს შემდეგი case-ების შესრულება.
//! 6) default არის დეპოლტური საქციელი როდესაც არცერთი case არ შეესაბამება — გამოიყენება როგორც else.


//! 7) შეიყვანეთ რიცხვი და შეამოწმეთ 0, 0-ზე მეტი, ან უარყოფითი (without parse).
{
    const num = Number(prompt("შეიყვანეთ რიცხვი:"));
    switch (true) {
        case num === 0:
            console.log("რიცხვი არის 0.");
            break;
        case num > 0:
            console.log("რიცხვი არის 0-დან მეტი.");
            break;
        default:
            console.log("რიცხვი არის უარყოფითი.");
    }
}

//! 8) დღეების გამოსახვა რიცხვის მიხედვით (1-7). თუ >7 ან <1 -> "არასწორი რიცხვი".
